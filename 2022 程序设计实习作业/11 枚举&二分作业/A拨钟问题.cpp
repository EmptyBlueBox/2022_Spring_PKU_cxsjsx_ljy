#include <iostream>
#include <cstring>
using namespace std;

int main() {
	int i, j;
	int a[10] = { 0 }, p[10] = { 0 };//a[i]是初始状态，p[i]是做操作i几次
	for (i = 1; i <= 9; i++)
		cin >> a[i];
	for (p[1] = 0; p[1] <= 3; p[1]++) {
		for (p[2] = 0; p[2] <= 3; p[2]++) {
			for (p[3] = 0; p[3] <= 3; p[3]++) {
				for (p[4] = 0; p[4] <= 3; p[4]++) {
					for (p[5] = 0; p[5] <= 3; p[5]++) {
						for (p[6] = 0; p[6] <= 3; p[6]++) {
							for (p[7] = 0; p[7] <= 3; p[7]++) {
								for (p[8] = 0; p[8] <= 3; p[8]++) {
									for (p[9] = 0; p[9] <= 3; p[9]++) {
										if ((p[1] + p[2] + p[4] + a[1]) % 4==0 &&//本来有a[1]，再加p的1、2、4的操作是4的倍数
										        (p[1] + p[2] + p[3] + p[5] + a[2]) % 4==0 &&
										        (p[2] + p[3] + p[6] + a[3]) % 4==0 &&
										        (p[1] + p[4] + p[5] + p[7] + a[4]) % 4==0 &&
										        (p[1] + p[3] + p[5] + p[7] + p[9] + a[5]) % 4==0 &&
										        (p[3] + p[5] + p[6] + p[9] + a[6]) % 4==0 &&
										        (p[4] + p[7] + p[8] + a[7]) % 4==0 &&
										        (p[5] + p[7] + p[8] + p[9] + a[8]) % 4==0 &&
										        (p[6] + p[8] + p[9] + a[9]) % 4==0) {
											for (i = 1; i <= 9; i++)
												if (p[i] != 0)
													for (j = 1; j <= p[i]; j++)
														cout << i << " ";
											return 0;
										}
									}
								}
							}
						}
					}
				}
			}
		}
	}
}